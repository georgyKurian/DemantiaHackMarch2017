package model;

import java.util.Date;

/**
 *
 * @author Georgi
 */
public class QuizAttempt {

    private int id;
    private String username;
    private Date date;

    public QuizAttempt(int id, String username, Date date) {
        this.id = id;
        this.username = username;
        this.date = date;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

}
